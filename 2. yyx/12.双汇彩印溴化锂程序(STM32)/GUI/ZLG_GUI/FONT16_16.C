/****************************************************************************
* 名称：GUI_PutString8_16()
* 功能：输出显示字符串((8*8字体，没有自动换行功能)。
* 入口参数： x		指定显示位置，x坐标
*           y		指定显示位置，y坐标
*           str		要显示的ASCII码字符串
* 出口参数：无
* 说明：操作失败原因是指定地址超出有效范围。
****************************************************************************/
void  GUI_PutString8_16(uint32 x, uint32 y, char *str)
{  
    while(1)
    {    
        if( (*str)=='\0' ) break;
        if( GUI_PutChar8_16(x, y, *str++,0)==0 ) break;
        x += 8;								// 下一个字符显示位置，y不变(即不换行)
    }
}
uint8 GUI_PutChar8_16(uint32 x, uint32 y, uint8 ch, UINT8 type)
{	
	uint8   font_dat;
   	uint32   i,j,k;
   	TCOLOR  bakc;  
   	
   	 	

   	/* 参数过滤 */
   	if( x>(GUI_LCM_XMAX-8) ) return(0);
   	if( y>(GUI_LCM_YMAX-8) ) return(0);
  	if( (ch<0x20) || (ch>0x7f) ) ch = 0x20;
  	//
    if (type)		//需要反显示时，交换前景色与背景色
	{
		GUI_ExchangeColor();
	}
	//  	
   	for (k=0; k<2; k++)
   	{   		
	   	for(i=0; i<8; i++)
	   	{  /* 读取点阵数据 */	   		
	    	font_dat = FONT8x16ASCII[(ch-0x20)*16+i+k*8];
	      
	      	for(j=0; j<8; j++)
	      	{  /* 设置相应的点为color或为back_color */
	         	if( (font_dat&DCB2HEX_TAB[j])==0 ) 
	         	{
	         		GUI_CopyColor(&bakc, back_color);
	         	}
	            else
	            {  
	            	GUI_CopyColor(&bakc, disp_color);
	            }
	         	GUI_Point(x, y, bakc);       
	         	x++;
	      	}
	      
	      	y++;									// 指向下一行
	      	x -= 8;									// 恢复x值
	   	}
   	}
   	//
   	if (type)		//需要反显示时，交换前景色与背景色
	{
		GUI_ExchangeColor();
	}
	//
   	return(1);
}

/*
竖显示字串
自动向右换行
type的低半字节表示是否反显，高半字节表示是左向右换行或从右向左换行
*/
void GUI_PutStringTier(UINT32 x, UINT32 y, char *str, UINT8 type)
{
	UINT32 len,index;
	//
	if (type & 0x0f)		//如果反显了，需要恢复前景色与背景色
	{
		GUI_ExchangeColor();
	}
	//
	len = strlen(str);
	//
	while (len>0)
	{
		//换行判断
		if ((y+16) > GUI_LCM_YMAX) 
		{
			if (type & 0xf0)
			{
				x -= 16;
			}
			else
			{
				x += 16;
			}
			y = 0;
		}
		//
		if (((UINT8)*str>0x80) && ((UINT8)*(str+1)>0x80))
		{			
			//
			for (index=0; index<(sizeof(GB_16)/sizeof(typFNT_GB16)); index++)
			{
				if (	(*str == (INT8)(GB_16[index].Index[0])) 
					&& (*(str+1) == (INT8)(GB_16[index].Index[1])))
				{
					break;
				}
			}
			//
			if (index == (sizeof(GB_16)/sizeof(typFNT_GB16)))
			{
				GUI_PutChar8_16(x,y,'?',0);
				GUI_PutChar8_16(x+8,y,'?',0);
				
			}
			else
			{
				GUI_PutHZ(x,y,(UINT8*)GB_16[index].Msk,16,16);
				
			}
			//
			y += 16;
			str += 2;
			len -= 2;
		}
		else
		{
			GUI_PutChar8_16(x,y,*str,0);
			str++;
			len--;
			//y += 8;
			y += 16;
		}
		
	}
	//
	if (type & 0x0f)	//如果反显了，需要恢复前景色与背景色，
	{
		GUI_ExchangeColor();
	}
	//
	//置上立即刷新显示标志
	//OS_ENTER_CRITICAL();
	//lcd_need_refurbish = 1;
	//OS_EXIT_CRITICAL();	
}

