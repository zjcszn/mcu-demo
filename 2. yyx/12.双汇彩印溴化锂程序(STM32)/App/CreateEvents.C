#include "app_cfg.h"

//*
//********************************************************************************************************
//* 函 数 名 : void CreateEvents(void)
//**------------------------------------------------------------------------------------------------------
//* 描    述 : 任务事件建立函数,事件类型可以是semaphores, mailboxes, queues, mutexes and event flags.
//**------------------------------------------------------------------------------------------------------
//* 输入参数 : None.
//**------------------------------------------------------------------------------------------------------
//* 输出参数 : None.
//**------------------------------------------------------------------------------------------------------
//* 返   回  : None.
//********************************************************************************************************
//*
void CreateEvents(void)
{
    INT8U  err;
    
    //NAND FLASH 信号量
    Sem_NAND_Flash = OSSemCreate(1);
    OSEventNameSet(Sem_NAND_Flash,"Sem_NAND_Flash",&err);
    
    //RTC信号量
    Sem_RTC = OSSemCreate(1);
    OSEventNameSet(Sem_RTC,"Sem_RTC_Timer",&err);
}

