u16 mb_crc16(u8* puchMsg,u8 usDataLen)
{
	u8 uchCRCHi = 0xFF;
	u8 uchCRCLo = 0xFF;
	unsigned int  uIndex;
	while (usDataLen--)
	{
		uIndex = uchCRCLo ^ *puchMsg++ ; /* 计算CRC */
		uchCRCLo = uchCRCHi ^ mb_auchCRCHi[uIndex];
		uchCRCHi = mb_auchCRCLo[uIndex] ;
	}
	return (((unsigned int)(uchCRCHi) << 8) | uchCRCLo) ;
}
