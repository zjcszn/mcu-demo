#include	"delay.h"



void Delay_us(unsigned char us)		//@22.1184MHz
{
	unsigned char i = 3,j;
	
	for(j=0;j<us;j++)
	{
		while (--i);
	}
}

void Delay_ms(unsigned char ms)		//@22.1184MHz
{
	unsigned char i = 22,j = 128,k;
	
	for(k=0;k<ms;k++)
	{
		_nop_();
		i = 22;
		j = 128;
		do
		{
			while (--j);
		} while (--i);
	}
}